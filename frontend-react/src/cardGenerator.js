function getFlavorVisual(flavor) {
  const key = (flavor || 'neutral').toLowerCase();
  return {
    colors: flavorsColors[key] || flavorsColors.neutral,
  };
}


function safe(text) {
  return String(text || '')
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');
}

